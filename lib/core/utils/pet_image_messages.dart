  /// Get image pool based on time/weekday
  static List<String> getImagePool() {
    final now = DateTime.now();
    final hour = now.hour;
    final weekday = now.weekday;
    final isWeekend = weekday == 6 || weekday == 7;

    if (isWeekend) {
      return getWeekendPool();
    }
    return getTimePool(hour);
  }

