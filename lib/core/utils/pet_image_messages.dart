  /// Get random message for an image
  static String getMessage(String imageName) {
    final name = imageName.replaceAll('.png', '').replaceAll('.webp', '');
    final messages = _messages[name];
    if (messages == null || messages.isEmpty) {
      return '甜甜在这里陪你哦～';
    }
    return messages[_random.nextInt(messages.length)];
  }

