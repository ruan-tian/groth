  /// Get random message for an image
  static String getMessage(String imageName) {
    final name = imageName.replaceAll('.png', '');
    final messages = _messages[name];
    if (messages == null || messages.isEmpty) {
      return '甜甜在这里陪你哦～';
    }
    return messages[_random.nextInt(messages.length)];
  }

  /// Get all image names
  static List<String> get allImageNames => _messages.keys.toList();

  /// Get image pool based on time/weekday
  static List<String> getImagePool() {
    final now = DateTime.now();
    final hour = now.hour;
    final weekday = now.weekday;
    final isWeekend = weekday == 6 || weekday == 7;

    if (isWeekend) {
      return getWeekendPool();
    }
    return getTimePool(hour);
  }

