  // ── Cover Matching ──
  static String coverForTitle(String title) {
    final text = title.toLowerCase();
    if (_containsAny(text, const ['lofi', 'lo-fi', '日常', '轻音', 'chill'])) {
      return coverLofi;
    }
    if (_containsAny(text, const ['relax', 'cafe', 'coffee', '放松', '咖啡'])) {
      return coverRelax;
    }
    if (_containsAny(text, const ['sleep', 'night', '晚安', '助眠', '睡眠', '入睡'])) {
      return coverSleep;
    }
    if (_containsAny(text, const [
      'study',
      'focus',
      'learn',
      '学习',
      '专注',
      '阅读',
      '功课',
    ])) {
      return coverStudy;
    }
    if (_containsAny(text, const [
      'fitness',
      'workout',
      'sport',
      'run',
      '运动',
      '健身',
      '训练',
      '跑步',
    ])) {
      return coverFitness;
    }
    if (_containsAny(text, const [
      'rain',
      'white noise',
      'noise',
      '雨',
      '白噪音',
      '下雨',
      '雨声',
    ])) {
      return coverRain;
    }
    if (_containsAny(text, const [
      'morning',
      'sunrise',
      'wake',
      '晨间',
      '清晨',
      '早晨',
      '唤醒',
    ])) {
      return coverMorning;
    }
    return coverDefault;
  }

